import numpy as np
from itertools import product

maps = {}
def get_map(name):
    walls = np.copy(maps[name])
    obstacles = []
    for r, c  in product(*map(range, walls.shape)):
        if walls[r, c] == 1:
            obstacles.append((r, c))
    return (set(obstacles), walls)

